    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        // TODO add your handling code here:
        String nombre = tfNombre.getText();
        String apellidos = tfApellidos.getText();
        String dni = tfDni.getText();
        
        System.out.println("Nombre" + nombre + " Apellido " + apellidos +  " Dni " + dni);

        boolean comprobar = RepasoTema8Ejercicio2.comprobar(nombre, apellidos, dni);
        
        if(!comprobar){
            JOptionPane.showMessageDialog(this, "Se ha a√±adido correctamente");
            tfNombre.setText("");
            tfApellidos.setText("");
            tfDni.setText("");
            lError.setText("");
        }else{
            lError.setText("Ese usuario ya existe!");
        }
        
    }//GEN-LAST:event_bAceptarActionPerformed

